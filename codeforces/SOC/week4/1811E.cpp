template <typename T>
T gcd(T a, T b)
{
    if (a % b == 0)
    {
        return b;
    }
    else
    {
        return gcd(b, a % b);
    }
}

void adv_tokenizer(string s, char del) // Split string
{
    stringstream ss(s);
    string word;
    while (!ss.eof())
    {
        getline(ss, word, del);
        cout << word << endl;
    }
}

void solve()
{
    ll k;
    cin >> k;
    ll n = 0;
    while (pow(9, n) < k)
    {
        n++;
    }
    // n = numof digits in number
    ll ans = 0;
    ll pow = 1;
    while (k > 0)
    {
        if (k % 9 <= 3)
        {
            ans += (k % 9)*pow;
        }
        else
        {
            ans += (k % 9 + 1)*pow;
        }
        k = k / 9;
        pow = pow*10;
    }
    cout << ans << endl;
}

